#include "main.h"
/**
 * print_diagonal - Affiche une diagonale de '\'.
 * @n: Nombre de fois que le caractère '\' est imprimé.
 */
void print_diagonal(int n)
{
	int i, j;

	if (n <= 0)
	{
		_putchar('\n');
		return;
	}

	for (i = 0; i < n; i++)
	{
		for (j = 0; j < i; j++)
		{
			_putchar(' ');
		}
		_putchar('\\');
		_putchar('\n');
	}
}
